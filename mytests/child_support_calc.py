            print(f"{parent.capitalize()} - Total Deviations: {self.deviations[parent]['total_deviation']}")
            print(f"{parent.capitalize()} - Health and Child Care Costs: {self.health_child_care_costs[parent]}")
            print(f"{parent.capitalize()} - Self Employment Income: {self.self_employment[parent]}")

