    def input_basic_info(self):
        for parent in ['mother', 'father']:
            print(f"Entering information for {parent}:")
            self.basic_info[parent]['name'] = input(f"Enter {parent}'s name: ")
            self.children_count = int(input(f"Enter number of children for {parent}: "))

    def calculate_gross_income(self):
        for parent in ['mother', 'father']:
            self.gross_income[parent] = float(input(f"Enter gross income for {parent}: "))

    def calculate_adjusted_income(self):
        for parent in ['mother', 'father']:
            # Deduct specific amounts as outlined, such as self-employment taxes, preexisting orders, etc.
            self_employment_tax = float(input(f"Enter self-employment tax for {parent} (if applicable): "))
            preexisting_support = float(input(f"Enter amount for preexisting child support for {parent} (if applicable): "))
            theoretical_support = float(input(f"Enter amount for theoretical child support for {parent} (if applicable): "))
            self.adjusted_income[parent] = self.gross_income[parent] - (self_employment_tax + preexisting_support + theoretical_support)

    def input_health_child_care_costs(self):
        for parent in ['mother', 'father']:
            health_costs = float(input(f"Enter health insurance costs for {parent}: "))
            child_care_costs = float(input(f"Enter work-related child care costs for {parent}: "))
            self.health_child_care_costs[parent] = {'health_insurance': health_costs, 'child_care': child_care_costs}

    def input_deviations(self):
        for parent in ['mother', 'father']:
            # Example deviation might include high income, special expenses etc.
            deviation = float(input(f"Enter total deviations for {parent} (if applicable): "))
            self.deviations[parent] = {'total_deviation': deviation}

    def calculate_self_employment(self):
        for parent in ['mother', 'father']:
            revenue = float(input(f"Enter total business revenue for {parent}: "))
            expenses = float(input(f"Enter total business expenses for {parent}: "))
            self.self_employment[parent] = revenue - expenses

    def calculate_child_support(self):
        combined_income = sum(self.adjusted_income.values())
        income_brackets = sorted(self.child_support_table.keys())
        closest_bracket = min(income_brackets, key=lambda x: abs(x-combined_income))
        base_support = self.child_support_table[closest_bracket][self.children_count - 1]
        
        for parent in ['mother', 'father']:
            pro_rata = self.adjusted_income[parent] / combined_income
            total_support = base_support * pro_rata
            health_care = self.health_child_care_costs[parent]['health_insurance'] + self.health_child_care_costs[parent]['child_care']
            adjusted_support = total_support + health_care - self.deviations[parent]['total_deviation']
            print(f"{parent.capitalize()} Child Support Payment: ${adjusted_support:.2f}")

    def display_results(self):
        print("Child Support Calculation Results:")
